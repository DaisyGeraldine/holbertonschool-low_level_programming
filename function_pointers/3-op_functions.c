#include <stdio.h>
/**
 * op_add - add two numbers
 * @a : number one
 * @b : number two
 * Return: sum a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - rest two numbers
 * @a : number one
 * @b : number two
 * Return: sum a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplique two numbers
 * @a : number one
 * @b : number two
 * Return: sum a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division two numbers
 * @a : number one
 * @b : number two
 * Return: sum a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - division two numbers
 * @a : number one
 * @b : number two
 * Return: sum a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
